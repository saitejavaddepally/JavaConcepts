package CompProgramming.BitManipulation;

public class LRShift {
    
    // left shift 
    // shifting the bits to left and appending 0 at the beginning
    // N = N<<1 1 time is shifted left
    // N = N * 2^i
    public static void main(String[] args) {
        // left shift check
        byte result = 127;
        System.out.println(result<<1);
        
    }

}
 